    public void llenarTabla() {
        EntityManager em = emf.createEntityManager();
        Query q = em.createQuery("SELECT h FROM HistoricoDatos h ORDER BY h.id DESC");
        List<HistoricoDatos> listaDatos = q.getResultList();
        
        DefaultTableModel model = (DefaultTableModel) jtableDatos.getModel();
        
        int numDatos = model.getRowCount();
        for (int i = 0; i < numDatos; i++) {
            model.removeRow(0);
        }
        
        int i = 0;
        for (HistoricoDatos h : listaDatos) {
            int idSensor = h.getSensor().getId();
            double valorTomado = h.getValorTomado();
            String fecha = h.getFecha();
            String hora = h.getHora();
            String procesamiento = h.getProcesamiento();

            model.addRow(new Object[]{idSensor,
                valorTomado,
                fecha,
                hora,
                procesamiento});
            i++;
            if (i == 5) {
                break;
            }
        }

    }

